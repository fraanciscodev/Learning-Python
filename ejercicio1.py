# 1. Operaciones Matemáticas Simples
# Escribe un programa que sume, reste, multiplique y divida dos números.

numero1 = float(input("ingresa un numero : "))
numero2 = float(input("ingresa un numero : "))


# Suma
suma = numero1 + numero2
print(suma)
print(f"El resultado de la suma: {numero1} + {numero2} = {suma}")

# Resta
resta = numero1 - numero2
print(resta)
print(f"El resultado de la resta: {numero1} - {numero2} = {resta}")

# Multiplicación
multiplicacion = numero1 * numero2
print("la multiplicacion es ", multiplicacion)
print(f"El resultado de la multiplicación: {numero1} * {numero2} = {multiplicacion}")

# División
division = numero1 / numero2
print("el valor de la división es ", division)
print(f"El resultado de la división: {numero1} / {numero2} = {division}")