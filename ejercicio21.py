# Crea una calculadora simple que permita sumar, restar, multiplicar y dividir dos números.

a = int(input("ingresa una numero cualquiera : "))
b = int(input("ingresa una numero cualquiera : "))

# Suma
print(f"Suma: {a + b}")

# Resta
print(f"Resta: {a - b}")

# Multiplicación
print(f"Multiplicación: {a * b}")

# División
print(f"División: {a / b}")